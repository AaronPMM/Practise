int sum(int *array, int length)
{
    int result;
    if (length <= 1){
        return array[0];
    }else{
        result = array[0] + sum(&array[1], length - 1);
    }
}

int main(void)
{
    int array[] = {2,3,4,5,6};
    int array_length = sizeof(array) / sizeof(array[0]);
    int sum_result = sum(array, array_length);
    printf("sum_result = %d\r\n", sum_result);
    return 0;
}
